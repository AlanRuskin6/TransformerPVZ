#pragma once

#include <cstdint>

// ============================================================================
// PVZ 游戏函数接口 - 完全参考 AsmVsZombies (AVZ)
// ============================================================================

namespace PVZ {

// ============================================================================
// 基础地址读取
// ============================================================================
uintptr_t GetBase();
uintptr_t GetBoard();
uintptr_t GetGameUI();

// ============================================================================
// 游戏状态读取
// ============================================================================
int GetSun();
int GetWave();
int GetScene();
bool IsInGame();
bool IsZombieInHouse();
bool IsCardSelectReady();                     // 选卡界面是否准备好（动画完成）
bool IsGamePaused();                          // 游戏是否暂停（包括菜单打开）

// ============================================================================
// 坐标转换函数 (参考 AVZ GridToAbscissa/GridToOrdinate)
// ============================================================================
int GridToAbscissa(int row, int col);  // 行列转X像素坐标
int GridToOrdinate(int row, int col);  // 行列转Y像素坐标

// ============================================================================
// 植物操作函数 (参考 AVZ avz_asm.cpp)
// ============================================================================
bool PutPlant(int row, int col, int type);   // 种植物
void RemovePlant(uintptr_t plant);            // 移除植物
bool Shovel(int row, int col);               // 铲植物
void ReleaseMouse();                          // 释放鼠标

// ============================================================================
// 炮操作函数
// ============================================================================
bool FireCob(int x, int y, int rank);        // 发射玉米炮

// ============================================================================
// 卡片操作函数
// ============================================================================
int GetCardMoveState(int cardType);          // 获取卡片移动状态 (0=移向卡槽,1=在卡槽,2=移下卡槽,3=在选卡界面)
bool IsCardInSlot(int cardType);             // 卡片是否已在卡槽中
bool ClearSelectedCards();                   // 清空已选卡片
bool ChooseCard(int cardType);               // 选卡 (普通植物 0-47)
bool ChooseImitatorCard(int cardType);       // 选模仿者卡 (目标植物类型)
bool PlantCard(int x, int y, int index);     // 使用卡片

// ============================================================================
// 游戏速度控制
// ============================================================================
int GetTickMs();                              // 获取每帧毫秒数 (默认10ms)
bool SetTickMs(int ms);                       // 设置每帧毫秒数 (最小1, 无上限)
float GetGameSpeed();                         // 获取游戏速度倍率 (10/TickMs)
bool SetGameSpeed(float speed);               // 设置游戏速度 (0.05-10倍)

// 帧倍增 - 在一帧内执行多次游戏逻辑更新，实现超高速
int GetFrameMultiplier();                     // 获取帧倍增数 (默认1)
bool SetFrameMultiplier(int mult);            // 设置帧倍增 (1-100)
void RunGameFrame();                          // 执行一帧游戏逻辑

// ============================================================================
// 游戏控制函数
// ============================================================================
bool Rock();                                  // 开始游戏（Let's Rock）
bool MakeNewBoard();                          // 重置关卡/创建新Board
bool CheckFightExit();                        // 检查退出战斗
bool BackToMain();                            // 返回主菜单 (仅从游戏中)
bool CloseOptionsScreen();                    // 关闭选项/挑战界面 (UI=7)

// ============================================================================
// 鼠标模拟函数 (参考 AVZ AAsm::MouseClick)
// 使用游戏内部鼠标处理，不影响真实鼠标
// ============================================================================
void MouseDown(int x, int y, int key);        // 鼠标按下
void MouseUp(int x, int y, int key);          // 鼠标松开
void MouseClick(int x, int y, int key);       // 鼠标点击 (按下+松开)
void LeftClick(int x, int y);                 // 左键点击

// ============================================================================
// 窗口信息
// ============================================================================
int GetWindowWidth();                         // 获取窗口宽度
int GetWindowHeight();                        // 获取窗口高度

// ============================================================================
// 物品收集 (参考 AVZ AItemCollector)
// ============================================================================
int CollectAllItems();                        // 收集所有物品(阳光/金币)，返回收集数量

// ============================================================================
// 冒险模式关卡控制
// ============================================================================
int GetLevel();                               // 获取当前关卡 (1-50)
bool SetLevel(int level);                     // 设置关卡 (1-50)
bool UnlockAllPlants();                       // 解锁所有植物

// ============================================================================
// 游戏模式控制 - 进入生存模式等
// ============================================================================
// GameMode 枚举值:
//   0 = 冒险模式
//   1-5 = 生存普通 (白天/黑夜/泳池/迷雾/屋顶)
//   6-10 = 生存困难 (白天/黑夜/泳池/迷雾/屋顶)
//   11-15 = 生存无尽 (白天/黑夜/泳池/迷雾/屋顶)
//   13 = 生存无尽泳池 (Pool Endless)
int GetGameMode();                            // 获取当前游戏模式
bool SetGameMode(int mode);                   // 设置游戏模式
bool StartGame(int mode);                     // 进入指定游戏模式并开始
void ProcessPendingActions();                 // 处理需要在主线程执行的延迟动作

}  // namespace PVZ
